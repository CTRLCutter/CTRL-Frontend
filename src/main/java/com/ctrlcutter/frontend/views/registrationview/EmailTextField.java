package com.ctrlcutter.frontend.views.registrationview;

import com.vaadin.flow.component.icon.Icon;
import com.vaadin.flow.component.icon.VaadinIcon;
import com.vaadin.flow.component.textfield.TextField;
import com.vaadin.flow.data.value.ValueChangeMode;

public class EmailTextField extends TextField {

    public EmailTextField() {

        setLabel(getTranslation("registration_form_email"));
        setRequired(true);

        // Email Regex'es are fun.
        setPattern(
                "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])");
        setClassName("registrationFormField");

        Icon emailCheckIcon = VaadinIcon.CHECK.create();
        emailCheckIcon.setVisible(false);
        setSuffixComponent(emailCheckIcon);

        setValueChangeMode(ValueChangeMode.EAGER);
        addValueChangeListener(e -> {
            emailCheckIcon.setVisible(!isInvalid());
        });
    }
}
